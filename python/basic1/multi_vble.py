# Unpack a Collection

fruits = ["apple", "banana", "cherry"]
x, y, z = fruits
print(x)
print(y)
print(z)

# Many Values to Multiple Variables

x1, y1, z1 = "Orange", "Banana", "Cherry"
print(x1)
print(y1)
print(z1)

# One Value to Multiple Variables

x2 = y2 = z2 = "Orange"
print(x2)
print(y2)
print(z2)
